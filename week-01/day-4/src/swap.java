public class swap {
    public static void main(String[] args) {
        // Swap the values of the variables
        int a = 123;
        int b = 526;

        System.out.println(a);
        System.out.println(b);

        b = 123;
        a = 526;

        System.out.println(a);
        System.out.println(b);

    }
}
